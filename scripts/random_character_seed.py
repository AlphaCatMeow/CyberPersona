def generate_seed():
    """生成一个随机角色种子"""
    core = random.choice(personality_cores)
    hair = random.choice(hair_styles)
    skin = random.choice(skin_features)
    eye = random.choice(eye_features)
    clothing = random.choice(clothing_styles)
    profession, profession_desc = random.choice(professions)
    selected_hobbies = random.sample(hobbies, random.randint(2, 4))
    voice = random.choice(voice_styles)
    speech = random.choice(speech_habits)
    attachment = random.choice(attachment_styles)
    # 从 emotion_expressions 中各选一个
    emotion_expr = {k: random.choice(v) for k, v in emotion_expressions.items()}
    selected_quirks = random.sample(quirks, random.randint(1, 2))
    vuln_topic, vuln_desc = random.choice(vulnerabilities)
    scene = random.choice(opening_scenes)
    age = random.randint(19, 28)

    seed = {
        "personalityCore": core,
        "appearance": {
            "hair": hair,
            "skin": skin,
            "eye": eye,
            "clothing": clothing,
        },
        "age": age,
        "profession": profession,
        "professionDesc": profession_desc,
        "hobbies": selected_hobbies,
        "voiceStyle": voice,
        "speechHabits": speech,
        "attachmentStyle": attachment,
        "emotionExpression": emotion_expr,
        "quirks": selected_quirks,
        "vulnerability": {"topic": vuln_topic, "description": vuln_desc},
        "openingScene": scene,
        "seedId": random.randint(10000, 99999),
    }
    return seed


